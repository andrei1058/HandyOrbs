package com.andrei1058.handyorbs.core;

public class ParticleLoc {

    private float x;
    private float y;
    private float z;

    public ParticleLoc(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public float getX() {
        return x;
    }

    public float getZ() {
        return z;
    }

    public float getY() {
        return y;
    }
}
